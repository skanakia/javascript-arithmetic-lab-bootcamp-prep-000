function add(a, b) {
  var number = a + b
  return number
} 

function subtract(a, b) {
  var number = a - b
  return number
}

function multiply(a, b) {
  var number = a * b
  return number
}

function divide(a, b) {
  var number = a / b
  return number
}

function inc(n) {
  return n++
}

function dec(n) {
  var number = n--
  return number
}

